function power3(z); % декларираме функция с името power3 , приемаща аргумент z
a = []; % създаваме празна матрица
for i = 1:3*z; % създаваме цикъл for , който се изпълнява от i = 1 до i = 3*z 
out(i^3); % принтираме полученото число
a = [a,i^3]; % записваме резултата в матрица
endfor % край на цилкъла
a % връщаме матрицата
endfunction % край на функцията
